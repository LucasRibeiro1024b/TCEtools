package model;

public class UnidadeOrc {
	public String codigo_municipio;
	public int exercicio_orcamento;
	public String codigo_orgao;
	public String codigo_unidade;
	public String codigo_tipo_unidade;
	public String nome_unidade;
	public String tipo_administracao_unidade;
	
	
	public String getCodigo_municipio() {
		return codigo_municipio;
	}
	public void setCodigo_municipio(String codigo_municipio) {
		this.codigo_municipio = codigo_municipio;
	}
	public int getExercicio_orcamento() {
		return exercicio_orcamento;
	}
	public void setExercicio_orcamento(int exercicio_orcamento) {
		this.exercicio_orcamento = exercicio_orcamento;
	}
	public String getCodigo_orgao() {
		return codigo_orgao;
	}
	public void setCodigo_orgao(String codigo_orgao) {
		this.codigo_orgao = codigo_orgao;
	}
	public String getCodigo_unidade() {
		return codigo_unidade;
	}
	public void setCodigo_unidade(String codigo_unidade) {
		this.codigo_unidade = codigo_unidade;
	}
	public String getCodigo_tipo_unidade() {
		return codigo_tipo_unidade;
	}
	public void setCodigo_tipo_unidade(String codigo_tipo_unidade) {
		this.codigo_tipo_unidade = codigo_tipo_unidade;
	}
	public String getNome_unidade() {
		return nome_unidade;
	}
	public void setNome_unidade(String nome_unidade) {
		this.nome_unidade = nome_unidade;
	}
	public String getTipo_administracao_unidade() {
		return tipo_administracao_unidade;
	}
	public void setTipo_administracao_unidade(String tipo_administracao_unidade) {
		this.tipo_administracao_unidade = tipo_administracao_unidade;
	}
}
